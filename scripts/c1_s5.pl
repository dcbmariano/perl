my $OPERACAO;

# Subtracao: 5 menos 3, igual a 2
$OPERACAO = 5 - 3;
print $OPERACAO."\n";
 
# Multiplicacao: 2 vezes 3, igual a 6
$OPERACAO = 2 * 3;
print $OPERACAO."\n";
 
# Divisao: 4 dividido por 2, igual a 2
$OPERACAO = 4 / 2;
print $OPERACAO."\n";
 
# Expressao: 5 menos 3, vezes 2 vezes 3, dividido por 
# 4 dividido por 2, o resultado sera 6
 
$OPERACAO = (5 - 3) * (2 * 3) / (4 / 2);
print $OPERACAO."\n";